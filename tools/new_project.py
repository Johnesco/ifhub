def main():
    parser = argparse.ArgumentParser(description="Create a new IF Hub project.")
    parser.add_argument("title", help="Game title (quoted)")
    parser.add_argument("name", help="Project directory name (lowercase)")
    parser.add_argument("--engine", default="inform7",
                        choices=CREATABLE_ENGINES,
                        help="Engine type (default: inform7)")
    args = parser.parse_args()

    title = args.title
    name = args.name
    engine = args.engine
    project_dir = PROJECTS_DIR / name

    if project_dir.is_dir():
        print(f"ERROR: Directory already exists: {project_dir}", file=sys.stderr)
        sys.exit(1)

    spec = get_engine_spec(engine)
    label = spec.label if spec else engine

    print(f"Creating project: {title} ({name}) [{label}]")
    print(f"  Location: {project_dir}")
    print()

    # Create base directories
    project_dir.mkdir(parents=True, exist_ok=True)

    # Engine-specific scaffold
    if engine == "inform7":
        source_file = scaffold_inform7(project_dir, title, name)
    elif engine == "ink":
        source_file = scaffold_ink(project_dir, title, name)
    elif engine in ("wwwbasic", "qbjc", "applesoft"):
        source_file = scaffold_basic(project_dir, title, name, engine)
    elif engine == "twine":
        source_file = scaffold_twine(project_dir, title, name)
    else:
        print(f"ERROR: Unsupported engine: {engine}", file=sys.stderr)
        sys.exit(1)

    # Shared files
    write_gitignore(project_dir, engine)
    write_deploy_workflow(project_dir)

    print()
    print(f"Project created at: {project_dir}")
    print()
    print("Next steps:")
    print(f"  1. Edit {project_dir / source_file}")
    print(f"  2. Build:    python {I7_ROOT / 'tools' / 'pipeline.py'} {name}")
    print(f"  3. Publish:  python {I7_ROOT / 'tools' / 'publish.py'} {name}")


if __name__ == "__main__":
    main()
