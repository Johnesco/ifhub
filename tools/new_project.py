#!/usr/bin/env python3
"""Create a new IF Hub project with build, test, and web infrastructure.

Usage:
    python tools/new_project.py "Game Title" game-name [--engine ENGINE]

Supported engines: inform7 (default), ink, wwwbasic, qbjc, applesoft, twine.
Creates a complete project scaffold with source file, tests, CI, and CLAUDE.md.
"""

import argparse
import sys
import textwrap
from datetime import datetime
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from lib.paths import PROJECTS_DIR, I7_ROOT
from lib.config import ENGINE_REGISTRY, get_engine_spec

# Engines available for project creation (subset of ENGINE_REGISTRY)
CREATABLE_ENGINES = ["inform7", "ink", "wwwbasic", "qbjc", "applesoft", "twine"]


# ---------------------------------------------------------------------------
# Shared scaffolding (all engines)
# ---------------------------------------------------------------------------


def write_gitignore(project_dir, engine):
    """Write .gitignore appropriate for the engine."""
    lines = [
        "# Assembled site output (generated by build_site.py / CI)",
        "_site/",
        "",
        "# Pipeline state (generated by pipeline.py)",
        ".pipeline-state",
        "",
        "# Claude Code local settings",
        ".claude/",
        "",
        "# OS generated files",
        "$RECYCLE.BIN/",
        "[Dd]esktop.ini",
        ".DS_Store",
        "Thumbs.db",
        "*~",
        ".*.sw[op]",
        "",
        "# IDE",
        ".vscode/",
        ".idea/",
    ]
    if engine == "inform7":
        lines[1:1] = [
            "",
            "# Build output",
            "*.ulx",
            "*.i6",
            "*.materials/",
            "",
            "# Inform 7 IDE project wrappers",
            "*.inform/",
            "",
            "# Test output (generated)",
            "tests/inform7/walkthrough_output.txt",
        ]
    (project_dir / ".gitignore").write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_deploy_workflow(project_dir):
    """Write GitHub Actions deploy-pages.yml."""
    (project_dir / ".github" / "workflows").mkdir(parents=True, exist_ok=True)
    (project_dir / ".github" / "workflows" / "deploy-pages.yml").write_text(
        textwrap.dedent("""\
        name: Deploy to GitHub Pages

        on:
          push:
            branches: [main]
          workflow_dispatch:

        permissions:
          contents: read
          pages: write
          id-token: write

        concurrency:
          group: pages
          cancel-in-progress: false

        jobs:
          deploy:
            environment:
              name: github-pages
              url: ${{ steps.deployment.outputs.page_url }}
            runs-on: ubuntu-latest
            steps:
              - uses: actions/checkout@v4
              - uses: actions/configure-pages@v5
              - name: Assemble site
                run: |
                  mkdir -p _site
                  [ -d web ] && cp -r web/* _site/ || true
                  [ -d lib ] && cp -r lib _site/ || true
                  cp *.html _site/ 2>/dev/null || true
                  cp *.txt _site/ 2>/dev/null || true
                  cp *.ni _site/ 2>/dev/null || true
                  cp *.ink _site/ 2>/dev/null || true
                  cp *.json _site/ 2>/dev/null || true
                  cp *.bas _site/ 2>/dev/null || true
              - uses: actions/upload-pages-artifact@v3
                with:
                  path: _site
              - id: deployment
                uses: actions/deploy-pages@v4
    """),
        encoding="utf-8",
    )


